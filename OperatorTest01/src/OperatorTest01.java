public class OperatorTest01 {
    public static void main(String[] args){
        int a = 10;
        int b =3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        int k = 10;
        k++;
        System.out.println("k=" + k);
        //++出现在变量的后面
        int m = 10;
        int e = m ++;
        System.out.println("e=" +e);
        System.out.println("m="+m);
        //++出现在变量的前面
        int f = 10;
        ++f;
        System.out.println("f="+f);




    }

}
